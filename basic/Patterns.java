package com.te.basic;

public class Patterns {

	public static void main(String[] args) {
		
		for(int i=1;i<=5;i++) {//outer loop for no of rows
			
			for(int j=1;j<=8;j++) {//inner loop for no of columns
				
			System.out.print("*");
			
			}
			System.out.println(" ");
		}

	}

}
