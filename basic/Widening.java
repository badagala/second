package com.te.basic;

public class Widening {

	public static void main(String[] args) {
		byte b=45;
		short b1=b;
		int i=b1;
		long l=i;
		float f=l;
		double d=f;
		System.out.println(b);
		System.out.println(b1);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);

	}

}
